package Q4;

public class Point_2D {
    private float x = 0.0f;
    private float y = 0.0f;

    public Point_2D() {
    }

    public float getX() {
        return this.x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return this.y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
